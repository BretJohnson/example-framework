namespace ExampleFramework.Tooling;

public class PreviewClass : Example
{
    bool isAutoGenerated = false;

    public PreviewClass(string classFullName, string? displayName) : base(classFullName, displayName)
    {
    }

    public PreviewClass(string classFullName, bool isAutoGenerated) : base(classFullName, null)
    {
        this.isAutoGenerated = isAutoGenerated;
    }

    public override bool IsAutoGenerated => this.isAutoGenerated;
}
